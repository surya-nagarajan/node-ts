    inf_twitter_link VARCHAR (256),
    inf_instagram_link VARCHAR (256),
    inf_carousel_first_image VARCHAR (256),
    inf_carousel_second_image VARCHAR (256),
    inf_carousel_third_image VARCHAR (256),
    inf_carousel_video_link VARCHAR (256),
    inf_carousel_thumbnail_image_link VARCHAR (256),
    inf_business_type VARCHAR (256),
    inf_level_status VARCHAR (256) NOT NULL DEFAULT 'LEVEL_ZERO',
    inf_profile_verify_status VARCHAR (256),
    inf_profile_reject_reason VARCHAR (256),
    inf_starting_price VARCHAR (256),
    inf_rzp_account_id VARCHAR (256),
    inf_lastseen VARCHAR (256),
