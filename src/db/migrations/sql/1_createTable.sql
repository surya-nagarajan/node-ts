    inf_lastName VARCHAR (256),
    inf_gender VARCHAR (256),
    inf_phoneNumber NUMERIC,
    inf_email VARCHAR (256),
    inf_password VARCHAR (256),
    inf_createdAt VARCHAR (256),
    inf_description VARCHAR (10485760),
    inf_address VARCHAR(256),
    inf_city VARCHAR(256),
    inf_state VARCHAR(256),
    inf_gst_country VARCHAR (256),
    inf_pincode VARCHAR (256),
    inf_gst_no VARCHAR (256),
    inf_gst_doc_url VARCHAR (256),
    inf_legalbillingname VARCHAR (256), 
    inf_email_status BOOLEAN,
